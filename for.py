'''
In Python, we use a for loop to iterate over sequences such as lists, strings, dictionaries,
'''
#for loop Syntax
'''
for val in sequence:
    # body of the loop
'''

languages = ['Swift', 'Python', 'Go']

# access elements of the list one by one
for lang in languages:
    print(lang)

language = 'Python'
# iterate over each character in language
for x in language:
    print(x)

# iterate from i = 0 to i = 3
for i in range(4):
    print(i)